package ru.anykeyers.processor.state.domain.kinds.filter;

/**
 * Типы критериев фильтрации по возрасту
 */
public enum FilterAgeKind {

    /**
     * Старше указанного возраста
     */
    GREATER_THAN,

    /**
     * Младше указанного возраста
     */
    LESS_THAN,

    /**
     * Равен указанному возрасту
     */
    EQUALS

}
